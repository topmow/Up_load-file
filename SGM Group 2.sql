
create database SGM;
use SGM;
create table Major(
	Major_ID int not null primary key,
	Major_Name Varchar(50) not null,
	Major_Year int not null
);
create table Province(
	Province_ID int not null primary key,
	ProvinceName Varchar(50) not null
);
create table Student(
	Student_ID INT not null Primary key,
	MidName Varchar(20),
	FirstName Varchar(15) Not null,
	LastName Varchar(15) not null,
	Sex Char check (Sex IN('M','F','m','f')),
	Birthday date,
	Student_Phone_Number int,
	Address Varchar(50),
	Province_ID int,
	Email Varchar(50),
	Major_ID int not null ,
	foreign key (Major_ID) references Major(Major_ID),
	foreign key (Province_ID) references Province(Province_ID)
);

create table Teacher(
	Teacher_ID int not null primary key,
	Teacher_FirstName Varchar(15) Not null,
	Teacher_MidName Varchar(20),
	Teacher_LastName Varchar(15) not null,
	Teacher_Address Varchar(50),
	Teacher_Province int,
	Teacher_Email Varchar(50),
	Teacher_Phone_Number int
	foreign key (Teacher_Province) references Province(Province_ID)
);

create table Class(
	Class_ID int not null primary key,
	CreditHour int not null,
	Class_Name Varchar(50) not null,
	Teacher_ID int not null,
	foreign key (Teacher_ID) references Teacher(Teacher_ID)
);

create table Grade(
	Student_ID int not null,
	Class_ID int,
	Semester Varchar(50) not null,
	DailyScore int,
	MidtermEScore INT,
	FinalEScore INT,
	foreign key (Student_ID) references Student(Student_ID),
	foreign key (Class_ID) references Class(Class_ID)
);

Create table Schoolfee(
	SchoolFee_ID int not null Primary Key,  
	Semester varchar(50) not null,
	Student_ID int not null,
	ClassFee int,    
	OtherFeeName varchar(50) ,
	OtherFee int , 
	Have_Paid Char check (Have_Paid IN('Y','N','y','n')) ,
	foreign key (Student_ID) references Student(Student_ID)
);
Create table Scholarship  (
	Scholarship_ID int not null primary key,  
	Scholarship_Name Varchar(50) not null,
	Scholarship_prize int not null
);

create table Event(
	Event_ID int not null primary key,
	Event_Name Varchar(50) unique not null
);

create table Scholar_Major(
	Scholarship_ID int not null,
	Major_ID int,
	Semester varchar(50) not null,
	foreign key (Scholarship_ID) references Scholarship(Scholarship_ID),
	foreign key (Major_ID) references Major(Major_ID)
);

create table Teacher_Major(
	Teacher_ID int not null,
	Major_ID int not null,
	foreign key (Teacher_ID) references Teacher(Teacher_ID),
	foreign key (Major_ID) references Major(Major_ID)
);

create table Student_Scholar(
	Student_ID int not null,
	Scholarship_ID int,
	Semester varchar(50) not null,
	foreign key (Scholarship_ID) references Scholarship(Scholarship_ID),
	foreign key (Student_ID) references Student(Student_ID)
);

create table Student_Trainingpoint(
	Student_ID int not null,
	Event_ID int,
	Semester varchar(50) not null,
	EventPosition varchar(50),
	Point int,
	foreign key (Event_ID) references Event(Event_ID),
	foreign key (Student_ID) references Student(Student_ID)
);
